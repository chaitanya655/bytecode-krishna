package krish;

public class integer {

	public static void main(String[] args) {
	int a=0;	
	if(a>0)
	{
		System.out.println("NUM IS POSITIVE");
	}
	else if(a==0)
	{
		System.out.println("NUM IS ZERO");
	}
	else if(a<0)
	{
		System.out.println("NUM IS NEGATIVE");
	}
}

}
